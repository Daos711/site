// Группировка вопросов по разделам
export function getPhilosophySections(): PhilosophySection[] {
  const sectionsMap = new Map<number, PhilosophySection>();

  for (const q of philosophyQuestions) {
    if (!sectionsMap.has(q.sectionId)) {
      sectionsMap.set(q.sectionId, {
        id: q.sectionId,
        title: q.section,
        questions: []
      });
    }
    sectionsMap.get(q.sectionId)!.questions.push(q);
  }

  return Array.from(sectionsMap.values()).sort((a, b) => a.id - b.id);
}
